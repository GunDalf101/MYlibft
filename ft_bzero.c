#include "libft.h"

void	*ft_bzero(void *str, size_t n)
{
	size_t				i;
	unsigned char		*s;

	i = 0;
	s = str;
	while (i < n)
	{
		s[i] = '\0';
		i++;
	}
	return (s);
}
